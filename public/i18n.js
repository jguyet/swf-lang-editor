const SUPPORTED_LANGS = ['fr', 'en', 'es'];
const DEFAULT_LANG = 'fr';

function detectLang() {
  const stored = localStorage.getItem('swf-editor-ui-lang');
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  const browser = (navigator.language || '').slice(0, 2).toLowerCase();
  if (SUPPORTED_LANGS.includes(browser)) return browser;
  return DEFAULT_LANG;
}

let _currentLang = detectLang();

function t(key, ...args) {
  const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS[DEFAULT_LANG];
  const fallback = TRANSLATIONS[DEFAULT_LANG];
  let str = dict[key] || fallback[key] || key;
  for (let i = 0; i < args.length; i++) {
    str = str.replace(`{${i}}`, args[i]);
  }
  return str;
}

function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  _currentLang = lang;
  localStorage.setItem('swf-editor-ui-lang', lang);
  document.documentElement.lang = lang;
}

function getLang() {
  return _currentLang;
}

window.i18n = { t, setLang, getLang, SUPPORTED_LANGS, FLAGS: { fr: '🇫🇷', en: '🇬🇧', es: '🇪🇸' } };
